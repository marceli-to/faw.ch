    <p>«Eine lebensfreundliche Stadt braucht viel Mobilität» — «Du sollst für lebendige Stadträume sorgen!» — «Der motorisierte Individualverkehr muss als Teil des öffentlichen Verkehrs gedacht werden, erst recht, wenn die Autos in Zukunft selbstfahrend sind.» — «Die Digitalisierung gibt den Verkehrsplanern neue Instrumente in die Hand.» Die Aussagen in der hitzigen Diskussion mit Thomas Sauter-Servaes (Leiter Studiengang Verkehrssysteme ZHAW), Fritz Kobi (Verkehrsexperte), Andreas Sonderegger (Mitglied Gruppe Krokodil/IKE ZHAW) und Mark Würth (Amt für Stadtentwicklung Stadt Winterthur) waren vielfältig. Ausgerüstet mit diesen Behauptungen zogen am Samstag 45 Stadtwerkstätterinnen und -werkstätter zu Fuss, auf dem Velo, im Spezial-Ringbus und mit den öffentlichen Verkehrsmitteln los, den Strassenraum in Winterthur zu erkunden.</p>
