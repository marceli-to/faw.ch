    <p>Vorhang auf für das atemberaubend wilde Stadtmodell der Künstler Dominik Heim und Ron Temperli! Ganze zehn Monate haben die beiden mit viel Verpackungsmaterial gearbeitet und ein 8x15 Meter grosses, begehbares Stadtmodell wachsen lassen. Mitgewirkt haben auch interessierte Winterthurerinnen und Winterthurer – die sogenannten Stadtwerkstätter.</p>
    <p>Dominik Heim, geboren 1974, lebt und arbeitet als freischaffender Künstler mit Schwerpunkt Malerei in Winterthur. Ron Temperli, geboren 1975, lebt und arbeitet als freischaffender Künstler mit Schwerpunkt Malerei und Installation in Winterthur.</p>
